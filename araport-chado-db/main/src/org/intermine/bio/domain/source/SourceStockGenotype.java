package org.intermine.bio.domain.source;

public class SourceStockGenotype {

	private String stockName;
	private String stockUniqueName;
	private String stockUniqueAccession;
	private String genotypeName;
	private String genotypeUniqueName;
	private String genotypeUniqueAccession;

	public SourceStockGenotype() {

	}

	public String getStockName() {
		return stockName;
	}

	public void setStockName(String stockName) {
		this.stockName = stockName;
	}

	public String getStockUniqueName() {
		return stockUniqueName;
	}

	public void setStockUniqueName(String stockUniqueName) {
		this.stockUniqueName = stockUniqueName;
	}

	public String getStockUniqueAccession() {
		return stockUniqueAccession;
	}

	public void setStockUniqueAccession(String stockUniqueAccession) {
		this.stockUniqueAccession = stockUniqueAccession;
	}

	public String getGenotypeName() {
		return genotypeName;
	}

	public void setGenotypeName(String genotypeName) {
		this.genotypeName = genotypeName;
	}

	public String getGenotypeUniqueName() {
		return genotypeUniqueName;
	}

	public void setGenotypeUniqueName(String genotypeUniqueName) {
		this.genotypeUniqueName = genotypeUniqueName;
	}

	public String getGenotypeUniqueAccession() {
		return genotypeUniqueAccession;
	}

	public void setGenotypeUniqueAccession(String genotypeUniqueAccession) {
		this.genotypeUniqueAccession = genotypeUniqueAccession;
	}

}
