package org.intermine.bio.domain.source;

public class SourceAllele {

	private String alleleUniqueName;
	private String alleleName;
	private String alleleUniqueAccession;
	private String featureType;
	private String genotypeName;
	private String genotypeUniqueName;
	private String description;
	private String sequenceAlterationType;
	private String mutagen;
	private String alleleClass;
	private String inheritanceType;
	private String mutationSite;
	private String wildType;

	public SourceAllele() {

	}

	public String getAlleleUniqueName() {
		return alleleUniqueName;
	}

	public void setAlleleUniqueName(String alleleUniqueName) {
		this.alleleUniqueName = alleleUniqueName;
	}

	public String getAlleleName() {
		return alleleName;
	}

	public void setAlleleName(String alleleName) {
		this.alleleName = alleleName;
	}

	public String getAlleleUniqueAccession() {
		return alleleUniqueAccession;
	}

	public void setAlleleUniqueAccession(String alleleUniqueAccession) {
		this.alleleUniqueAccession = alleleUniqueAccession;
	}

	public String getFeatureType() {
		return featureType;
	}

	public void setFeatureType(String featureType) {
		this.featureType = featureType;
	}

	public String getGenotypeName() {
		return genotypeName;
	}

	public void setGenotypeName(String genotypeName) {
		this.genotypeName = genotypeName;
	}

	public String getGenotypeUniqueName() {
		return genotypeUniqueName;
	}

	public void setGenotypeUniqueName(String genotypeUniqueName) {
		this.genotypeUniqueName = genotypeUniqueName;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getSequenceAlterationType() {
		return sequenceAlterationType;
	}

	public void setSequenceAlterationType(String sequenceAlterationType) {
		this.sequenceAlterationType = sequenceAlterationType;
	}

	public String getMutagen() {
		return mutagen;
	}

	public void setMutagen(String mutagen) {
		this.mutagen = mutagen;
	}

	public String getAlleleClass() {
		return alleleClass;
	}

	public void setAlleleClass(String alleleClass) {
		this.alleleClass = alleleClass;
	}

	public String getInheritanceType() {
		return inheritanceType;
	}

	public void setInheritanceType(String inheritanceType) {
		this.inheritanceType = inheritanceType;
	}

	public String getMutationSite() {
		return mutationSite;
	}

	public void setMutationSite(String mutationSite) {
		this.mutationSite = mutationSite;
	}

	public String getWildType() {
		return wildType;
	}

	public void setWildType(String wildType) {
		this.wildType = wildType;
	}

}
